package com.example.demo.modal;


public class User {
	
	Long id;
	String userName;
	String firstName;
	String lastName;
	String email;
	String password;
	String phone;
	int calories_per_day;
	boolean userSattus;
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getFirstName() {
		return firstName;
	}
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public int getCalories_per_day() {
		return calories_per_day;
	}
	public void setCalories_per_day(int calories_per_day) {
		this.calories_per_day = calories_per_day;
	}
	public boolean isUserSattus() {
		return userSattus;
	}
	public void setUserSattus(boolean userSattus) {
		this.userSattus = userSattus;
	}
	
	

}
